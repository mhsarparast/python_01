class Student:
    def __init__(self, first_name, last_name,Year,code_melli,code_daneshjoo):
        self.first_name = first_name
        self.last_name = last_name
        self.Year = Year
        self.code_melli = code_melli
        self.code_daneshjoo = code_daneshjoo
    def save(self):
        print(self.first_name,self.last_name,"save")

